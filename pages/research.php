<h1>Research</h1>

<h2>Autonomic Dissemination of Information in Online Social Networks</h2>
	
<img src="media/research1.png" class="small-image right"/>

<p>I'm a PhD student at the <a href="http://cs.cf.ac.uk" target="_blank">School of Computer Science</a>, <a href="http://cf.ac.uk" 
target="_blank">Cardiff University</a>.</p>

<p>
My research is to do with the dissemination of content and 
information in online social networks - in particular, Twitter. 
I am supervised by 
<a href="http://users.cs.cf.ac.uk/Stuart.M.Allen" target="_blank" >Dr Stuart Allen</a> 
and 
<a href="http://users.cs.cf.ac.uk/R.M.Whitaker" target="_blank" >Prof Roger Whitaker</a>.
</p>

<p>
I am interested in how information is spread around through the act of retweeting. My work so far has focused
on the properties and behaviour of retweets and the ways in which different structures of a user's local networks can affect
the behaviour of the propagation of retweets within it.</p>

