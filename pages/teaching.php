<div class="column">
	<h2>Supervised Projects</h2>
	
	<h3>Digitial Asset Bank</h3>
	<p>Produce a design and prototype system representing a digital asset bank. Users are able to upload various types of electronic media (documents, images, movies, 3-D models, etc.), which would then be made available for other users to purchase and download. The system incorporated various algorithms for detcting popular assets and allowed for appropriate previews of assets before purchases are made.</p>
	
	<h3>Coursework Assistance Tool</h3>
	<p>Produce a design and prototype system to help students in managing and tracking their various University courseworks. The system was produced in PHP and enabled users to view upcoming deadlines, create and manage milestones, track and keep up-to-date with other group members, submit questions for staff, and so on.</p>
	
	<h3>Online Library System</h3>
	<p>Produce a design for an online system for a local library so that users can access eBooks, audiobooks, music, movies, etc online using their library account. This design was extended to work with mobile clients (Android, iOS, Kindle) and encompassed the legal aspects (copyright, etc).</p>
	
	<h3>Geo-Information Sharing for Mobile Devices</h3>
	<p>Produce a design for a system for smartphones (particularly Android, but extended to iOS) that would allow users to share geographical information on geographical features (buildings, points of interest, roads, natural features, etc) via a map-based positioning system. The design included how GPS would be used to locate the features, database design, how data would be shared & updated, and so on.</p>
